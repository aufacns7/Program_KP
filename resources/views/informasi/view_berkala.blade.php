<tr data-key="1"><td>1</td><td><a><b>Profil Kementerian Perhubungan</b></a><a><br><i>Deskripsi :</i><br></a><a><p>Kementerian Perhubungan memiliki struktur organisasi yang lengkap dan terkoordinasi. Penyusunan organisasi dan tata kerja Kementerian Perhubungan bertumpu pada prinsip birokrasi yang sederhana, namun memiliki kinerja yang efektif dan efisien serta optimal dalam menjalankan tugasnya.</p>
<p>Sejak 2021, terdapat beberapa tipe nomenklatur unit organisasi Kementerian Perhubungan, yakni Sekretariat Jenderal, Direktorat Jenderal, Badan, Inspektorat Jenderal, Staf Ahli, serta Pusat. Masingmasing unit organisasi memiliki kedudukan, tugas dan fungsi, serta bertanggung jawab kepada Menteri Perhubungan.</p>
<p>Menteri Perhubungan berkewajiban merumuskan, menetapkan, dan menjalankan kebijakan di bidang transportasi. Kebijakan ini menyangkut pelayanan, keselamatan, keamanan, peningkatan aksesibilitas, serta konektivitas sarana dan prasarana transportasi. Menteri Perhubungan turut memberikan bimbingan teknis serta supervisi pelayanan transportasi di daerah, agar mobilitas penduduk serta distribusi barang dan jasa terselenggara dengan nyaman, aman, selamat, mudah diakses, dan terkoneksi.</p>
<p>Sekretariat Jenderal (Setjen) Kementerian Perhubungan mempunyai tugas utama mengoordinasikan tugas seluruh unit organisasi di lingkungan Kementerian Perhubungan. Wewenang Setjen mencakup dukungan administrasi, perancangan anggaran, penyusunan peraturan perundangundangan, serta advokasi hukum.</p>
<p>Kementerian Perhubungan memiliki empat Direktorat Jenderal (Ditjen), yang masingmasing membawahi matra transportasi darat, laut, udara, dan perkeretaapian. Setiap ditjen memiliki wewenang menyusun dan menetapkan norma, standar, prosedur, serta kriteria penyelenggaraan transportasi di masing-masing matra, termasuk memberikan bimbingan teknis, supervisi, dan evaluasi penyelenggaraan layanan perhubungan. Tujuannya adalah membuat negara mampu menghadirkan layanan transportasi darat, laut, udara dan kereta api yang aman, nyaman, selamat, serta terpadu.</p>
<p>Selain keempat matra tersebut, penyelenggaraan transportasi memiliki perhatian khusus pada Daerah Khusus Ibukota Jakarta sebagai pusat pemerintahan dan perekonomian. Ini memicu konsentrasi mobilitas penduduk ke ibukota. Demi menghindari pemusatan dan konsentrasi sumber daya ke satu wilayah, pemerintah merancang konsep aglomerasi Jakarta, Bogor, Depok, Tangerang, dan Bekasi (Jabodetabek). Unit kerja Kementerian Perhubungan dengan tugas mengembangkan, mengelola, dan meningkatkan pelayanan transportasi di wilayah aglomerasi ini adalah Badan Pengelola Transportasi Jabodetabek (BPTJ).</p>
<p>Penyelenggaraan transportasi di Indonesia juga tidak bisa terlepas dari keberadaan satuan pengawas menjadi unsur utama tata kelola pemerintahan yang baik, bersih, dan melayani. Di Kementerian Perhubungan, fungsi tersebut dijalankan oleh Inspektorat Jenderal (Itjen). Unit organisasi ini merupakan satuan pengawasan internal di lingkup kementerian. Itjen secara rutin melakukan audit, tinjauan, evaluasi, pemantauan, dan kegiatan pengawasan lainnya di lingkungan Kementerian Perhubungan.</p>
<p>Tidak hanya melakukan pengawasan secara internal, Kementerian Perhubungan terus memperkuat penelitian dan pengembangan di bidang transportasi. Tujuannya agar pelaksanaan pembangunan transportasi nasional berbasiskan riset serta pendalaman dan telaah ilmiah. Unit organisasi di lingkungan Kementerian Perhubungan yang membidangi hal ini adalah Badan Penelitian dan Pengembangan Perhubungan.</p>
<p>Secara utuh, penyelenggaraan transportasi tidak luput dari peran sumber daya manusia (SDM) yang andal. Upaya terus menerus dan berkesinambungan dalam membentuk, mengasah, dan meningkatkan kualitas SDM dilakukan melalui penyelenggaraan sekolah tinggi, politeknik, serta pusat pendidikan yang berada di bawah koordinasi Badan Pengembangan Sumber Daya Manusia Perhubungan (BPSDM).</p>
<p>Menteri Perhubungan membutuhkan unit kerja khusus yang bertugas memberikan telaah, masukan, dan rekomendasi. Fungsi ini dijalankan oleh Staf Ahli. Unit kerja ini berisi cendekiawan, akademisi, praktisi, dan profesional di bidang transportasi.</p>
<p>Selain itu Kementerian Perhubungan juga memiliki tiga pusat yaitu Pusat Teknologi Informasi dan Komunikasi Perhubungan (Pustikomhub), Pusat Pengelolaan Transportasi Berkelanjutan (PPTB), serta Pusat Fasilitasi Kemitraan dan Kelembagaan Internasional (PFKKI). Pustikomhub bertugas mengoordinasikan, mengelola, mengembangkan, dan menjalankan sistem teknologi informasi dan komunikasi Kementerian Perhubungan, termasuk menyusun rencana kerja, anggaran, serta ketatausahaan di bidang data dan informasi. Pembangunan perhubungan dan transportasi nasional berdampingan dengan upaya menjaga lingkungan hidup, terutama</p>
<p>kemampuan adaptasi dan mitigasi sektor perhubungan terhadap perubahan iklim. Inilah fokus dan tugas utama dari Pusat Pengelolaan Transportasi Berkelanjutan (PPTB).</p>
<p>Pusat Fasilitasi Kemitraan dan Kelembagaan Internasional (PFKKI) merupakan perpanjangan tangan Kementerian Perhubungan dengan warga dunia. Unit kerja ini berperan aktif dalam menghadirkan kerja sama internasional. Mulai dari menjalin kemitraan global, membuka pintu masuk investasi, serta memfasilitasi ratifikasi dan konvensi internasional bidang transportasi.<br /><br /></p>
