                    <h1>The History of Ministry of Transportation</h1>
                    <p class=""><img alt="" class="img-fluid" src="https://portal.dephub.go.id/public/images/uploads/posts/kantor-kemenhub.png"></p>
                    <!-- /post meta -->
                    <div class="post-content">
                        <div class="dropcaps">
                            
                            <ul> <p style="text-align: justify;">Bentuk awal Kementerian
Perhubungan yang saat itu bernama Departemen Perhubungan yang lahir dalam
kancah perjuangan adalah gabungan antara Departemen Perhubungan dan Departemen
Pekerjaan Umum, yang dipimpin oleh seorang Menteri Abikusno Tjokrosuyoso. Namun
hal ini tidak berlangsung lama, karena Departemen Perhubungan dan Departemen
Pekerjaan Umum tidak lagi dijabat oleh orang yang sama yang merangkap tugas
seperti sebeumnya. Urusan perhubungan dan pekerjaan umum kemudian berada di
bawah dua pejabat yang berbeda yaitu Kementerian Perhubungan dipimpin oleh Ir. Abdulkarim dan Kementerian Pekerjaan Umum di bawah pimpinan Ir. Putuhena. Sesuai dengan nama yang
disandangnya, Departemen Perhubungan mengurusi masalah perhubungan. Keinginan
Belanda untuk berkuasa kembali di Indonesia sangat jelas terlihat ketika mereka
melancarkan agresi militernya yang kedua pada tanggal 19 Desember 1948. Dalam
agresinya tersebut, Belanda berhasil menguasai Yogyakarta dan menangkap
Presiden Soekarno dan Wakil Presiden Hatta. Dalam kondisi darurat ini, Dinas
Telegrap sebagi salah satu Jawatan dalam Departemen Perhubungan berhasil menjalankan
tugasnya yang sangat berdampak penting bagi kelangsungan tegaknya Indonesia
saat itu. Dinas Telegrap berhasil mengirim berita terakhir ke Bukittinggi yang
ditujukan kepada Mr. Sjafruddin Prawiranegara dari Presiden Soekarno yang
isinya memberi wewenang untuk membentuk suatu pemerintahan darurat. Selanjutnya
dibentuklah Kabinet Darurat dengan Mr. Sjafruddin Prawiranegara sebagai Perdana
Menteri dan Ir. Indratjaja sebagai Menteri Perhubungan dan merangkap sebagai
Menteri Kemakmuran. Sejak awal kemerdekaan hingga pengakuan kedaulatan Belanda
atas RIS tahun 1949, Departemen Perhubungan memiliki wewenang untuk mengatur
perhubungan laut, udara, darat, perkeretaapian serta pos, telegraf, dan
telekomunikasi dan masing-masing sektor tersebut diurus oleh jawatan-jawatannya
sendiri yang berada di bawah struktur organisasi Departemen Perhubungan.</p><p style="text-align: justify;"></p><p style="text-align: justify;">Titik berat yang menjadi
perhatian Departemen Perhubungan pada era 1945-1949 adalah perhubungan darat
karena diantara beberapa sektor perhubungan lainya seperti laut maupun udara
belum bisa menjadi sarana optimal. Angkatan laut masih terbatas jalur
operasinya karena sebagian besar wilayah lautan Indonesia dikuasai oleh tentara
sekutu, termasuk Belanda sehingga hubungan interinsuler ada dalam kekuasaan
mereka. Perkeretaapian menjadi perhatian utama dalam mengelola perhubungan
darat pada masa itu karena jaringan angkutan darat lainnya seperti bus, truk,
mobil dapat dikatakan tidak ada karena semuaalat angkut bermotor masih dikuasai Jepang.
Pembenahan perkeretaapian sebagai sarana darat utama saat itu bukanlah suatu
hal yang mudah untuk dilakukan sebab tingkat kerusakan kereta api cukup signifikan. Pada masa Demokrasi Liberal ini terjadi perubahan bagi
Departemen Perhubungan secara kelembagaan, yaitu dibentuknya Departemen Perhubungan Laut pada masa Kabinet Djuanda. Dengan adanya
departemen tersebut, maka urusan laut yang sebelumnya menjadi tanggung jawab
Departemen Perhubungan, kini menjadi terpisah dan ditangani secara mandiri oleh
Departemen Perhubungan Laut. Dalam rangka menguasai pelayaran secara
menyeluruh, diputuskan bahwa sudah tiba waktunya bagi pemerintah untuk
mendirikan perusahaan pelayaran. Oleh karena itu pada tahun 1952, Ir. Djuanda
selaku Menteri Perhubungan menetapkan berdirinya PT Pelayaran Nasional
Indonesia (Pelni) yang mulai beroperasi dengan 16 buah kapal milik Perpuska dan
45 kapal yang baru dibeli.</p><p style="text-align: justify;"></p><p style="text-align: justify;">Berdasarkan Keputusan Presiden RI No. 153 tanggal 10Juli 1959, Soekarno kemudian
membentuk kabinet baru yang dikenal dengan sebutan Kabinet Kerja I. Dalam
Kabinet Kerja I ini terjadi perubahan dalam struktur pemerintahan negara.
Istilah Departemen Perhubungan tidak lagi digunakan dan sebagai gantinya
dikenal dengan sebutan Kementrian Distribusi dengan J. Leimena menjabat sebagai
Menterinya. Perubahan yang cukup signifikan bagi Departemen Perhubungan
terjadi pada masa pemerintahan Demokrasi Terpimpin ini. Pada era ini, urusan
perhubungan laut, udara, dan darat serta komunikasi tidak lagi berada secara
keseluruhan di bawah wewenang Departemen Perhubungan, tetapi terpecah-pecah ke
dalam beberapa Departemen yang baru terbentuk pada era tersebut. Nama
Departemen Perhubungan dalam Kabinet Kerja I tidak lagi ada tetapi diganti
dengan istilah Bidang Distribusi yang menangani perhubungan laut, perhubungan
darat, pos, dan telekomunikasi, perhubungan udara serta perdagangan. Sedangkan
pada Kabinet Kerja II, urusan perhubungan berada di bawah bidang Distribusi
yang terdiri dari Departemen Perhubungan Darat, Pos dan Telekomunikasi,
Departemen Perhubungan Laut, Departemen Perhubungan Udara, dan Departemen
Perdagangan. Dalam Kabinet Kerja IV, istilah Bidang Distribusi diganti dengan
Kompartemen Distribusi yang antara lain terdiri dari Departemen Perhubungan
Darat, Pos dan Telekomunikasi, Departemen Perhubungan Laut dan Departemen
Perhubungan Udara. Sedangkan pada masa Kabinet Dwikora, perhubungan laut tidak
lagi masuk dalam Kompartemen Distribusi tetapi dibentuk kompartemen sendiri
yaitu Kompartemen Maritim yang meliputi Departemen Perhubungan Laut, Departemen
Perikanan dan Pengolahan Produksi Hasil Laut, serta Departemen Industri
Maritim. Perubahan terjadi lagi dalam Kabinet Dwikora yang Disempurnakan I,
yaitu terpisahnya urusan pos dan telekomunikasi dengan perhubungan darat. Dalma
kabinet ini dibentuk Departemen baru yaitu Departemen Pos dan Telekomunikasi
dibawah Kompartemen Distribusi. Selanjutnya dalam susunan Kabinet Dwikora yang
disempurnakan II kembali terjadi lagi perubahan. Urusan-urusan perhubungan yang
sebelumnya didistribusikan dalam beberapa departemen, kini disatukan kembali
dalam Departemen Perhubungan. Dalam masa awal Orde Baru Struktur Organisasi
Departemen Perhubungan dalam Kabinet Pembangunan I, II, dan III adalah meliputi
Menteri Perhubungan, Sekertaris Jenderal Departemen Perhubungan, Inspektorat
Jenderal Departemen Perhubungan, Direktorat Jenderal Perhubungan Laut dan
Direktorat Pariwisata Pos dan Telekomunikasi. Lembaga Metereologi dan Geofisika
berada di bawah Direktorat Perhubungan Udara sampai dengan tahun 1972 karena
pada tahun 1973 dipindahkan dari Direktorat Perhubungan Udara ke departemen
Perhubungan. Penataan demi penataan yang dilakukan dalam Departemen Perhubungan
dengan menyesuaikan struktur dan citranya, sehingga menjadi lebih baik dan
bermanfaat bagi bangsa dan negara. Konsolidasi yang dilakukan sejak tahun 1945
hingga sekarang menghasilkan sebuah Departemen yang besar.</p></ul><p style="text-align: justify;"></p>
